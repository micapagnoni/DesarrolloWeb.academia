//If: Sentencia de DECISIÓN: "Si pasa tal cosa, entonces pasa tal otra"

var edad = 18;

if (edad >= 18) {
    console.log("La persona es mayor de edad")
} else {
    console.log("La persona es menor de edad")
}

// en este caso "edad >= 18" sería la condición que estamos imponiendo
// si esa condición se cumple, entonces la persona es mayor de edad y eso es lo que va a imprimirse en pantalla.
//"else" significa "por el caso contrario" o "si no se cumple tal condición"